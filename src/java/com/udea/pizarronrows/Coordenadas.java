package com.udea.pizarronrows;

import java.util.logging.Logger;

public class Coordenadas {
    private float x;
    private float y;

    public Coordenadas(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
    
}
